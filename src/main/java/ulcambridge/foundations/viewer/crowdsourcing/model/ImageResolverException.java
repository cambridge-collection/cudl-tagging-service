package ulcambridge.foundations.viewer.crowdsourcing.model;

public class ImageResolverException extends Exception {
    public ImageResolverException() {
        super();
    }

    public ImageResolverException(String message) {
        super(message);
    }

    public ImageResolverException(String message, Throwable cause) {
        super(message, cause);
    }

    public ImageResolverException(Throwable cause) {
        super(cause);
    }
}
